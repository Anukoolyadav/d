public class multiply {
    public static int mul(int a,int b){
        int m = a*b;
        return m;
    }
    public static void main(String args[]){
        int a =4;
        int b =7;
        int x=mul(a, b);
        System.out.println("mul : "+x);
        int q=mul(4, 5);
        System.out.println("q is : "+q);
    }
    
}
